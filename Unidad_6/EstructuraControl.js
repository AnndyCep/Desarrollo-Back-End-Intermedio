// While
// Nos permite realizar una condicion hasta que se cumpla la condicion.

let numero = 0

while (numero <=10){
    console.log(numero)
    numero++
}

//for
//Permite ejecutar un codigo un numero determinado de veces
let num = 10
for (let i = 0 ; i < num ; i++){
    console.log(` el numero iterado es ${i}`)
}